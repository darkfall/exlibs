// ------------------------------------------------------------------
// Desc: 
// ------------------------------------------------------------------

Allocator_dl283::Allocator_dl283()
{
}

// ------------------------------------------------------------------
// Desc: 
// ------------------------------------------------------------------

Allocator_dl283::~Allocator_dl283()
{
}

// ------------------------------------------------------------------
// Desc: 
// ------------------------------------------------------------------

void* Allocator_dl283::alloc( size_t _size )
{
    return ::dlmalloc( _size );
}

// ------------------------------------------------------------------
// Desc: 
// ------------------------------------------------------------------

void* Allocator_dl283::allocAligned( size_t _size, uint _alignment )
{
    return ::dlmemalign( _alignment, _size );
}


// ------------------------------------------------------------------
// Desc: 
// ------------------------------------------------------------------

void* Allocator_dl283::realloc( void* _ptr, size_t _new_size )
{
    return ::dlrealloc( _ptr, _new_size );
}

// ------------------------------------------------------------------
// Desc: 
// ------------------------------------------------------------------

void* Allocator_dl283::reallocAligned( void* _ptr, size_t _new_size, uint _alignment )
{
    return ::dlrealloc( _ptr, _new_size );
}

// ------------------------------------------------------------------
// Desc: 
// ------------------------------------------------------------------

void Allocator_dl283::free( void* _ptr )
{
    ::dlfree(_ptr);
}


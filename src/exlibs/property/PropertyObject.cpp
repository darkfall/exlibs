void PropertyObject::serialize ( ISerializeNode* _pNode )
{
    PropertiesDescriptor* pDesc = getDescriptor();
    if ( pDesc )
        pDesc->serializePropertyObject ( _pNode, this );
    else
        ex_warning ( "the Descriptor is not found!" );
}

